export function pgT(locale: PortalLocale, key: string): string {
  return dicts[locale]?.[key] ?? dicts.en[key] ?? key
}

export function statusPgLabel(locale: PortalLocale, status: string): string {
  switch (status) {
    case "active":
      return pgT(locale, "active")
    case "disabled":
      return pgT(locale, "disabled")
    case "expired":
      return pgT(locale, "expired")
    case "limited":
      return pgT(locale, "limited")
    case "on_hold":
      return pgT(locale, "on_hold")
    default:
      return status
  }
}
